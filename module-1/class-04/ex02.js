// Explicação de switch... case

let opcao = 1

switch(opcao){
    case 1: 
        console.log('Você selecionou a primeira opção')
        break
    case 2: 
        console.log('Você selecionou a segunda opção')
        break
    default: 
     console.log('Você não escolheu nenhuma opção válida!')
     break
}